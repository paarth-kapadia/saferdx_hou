CREATE TABLE Dflt._pksfc_STEP01
(
	p_SSN VARCHAR(50) DEFAULT NULL
	,tl_sttpnt DATETIME2 DEFAULT NULL
	,tl_lookbp_months INT DEFAULT 0
	,diag_ICD10CM_id BIGINT DEFAULT -1
	,diag_ICD09CM_id BIGINT DEFAULT -1
)


INSERT INTO Dflt._pksfc_STEP01
(
	p_SSN
	,tl_sttpnt
	,tl_lookbp_months
	,diag_ICD10CM_id
	,diag_ICD09CM_id
)
SELECT DISTINCT
	pts.p_SSN
	,pts.tl_sttpnt
	,pts.tl_lookbp_months
	,owld.ICD10SID
	,owld.ICD9SID
FROM
	Src.SPatient_SPatient AS sp INNER JOIN Src.Outpat_WorkloadVDiagnosis AS owld ON
	(
		sp.PatientSID = owld.PatientSID
	)
		INNER JOIN Dflt._pksf_calc_elix__INPUT_patients AS pts ON
		(
			pts.p_SSN = sp.PatientSSN
		)
WHERE
	owld.VisitDateTime BETWEEN
		DATEADD(MONTH, -1 * pts.tl_lookbp_months, pts.tl_sttpnt)
		AND
		pts.tl_sttpnt

INSERT INTO Dflt._pksfc_STEP01
(
	p_SSN
	,tl_sttpnt
	,tl_lookbp_months
	,diag_ICD10CM_id
	,diag_ICD09CM_id
)
SELECT DISTINCT
	pts.p_SSN
	,pts.tl_sttpnt
	,pts.tl_lookbp_months
	,inpd.ICD10SID
	,inpd.ICD9SID
FROM
	Src.SPatient_SPatient AS sp INNER JOIN Src.Inpat_InpatientDiagnosis AS inpd ON
	(
		sp.PatientSID = inpd.PatientSID
	)
		INNER JOIN Dflt._pksf_calc_elix__INPUT_patients AS pts ON
		(
			pts.p_SSN = sp.PatientSSN
		)
WHERE
	inpd.DischargeDateTime BETWEEN
		DATEADD(MONTH, -1 * pts.tl_lookbp_months, pts.tl_sttpnt)
		AND
		pts.tl_sttpnt


GO


